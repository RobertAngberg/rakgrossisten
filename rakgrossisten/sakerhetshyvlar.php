<h3>Lär dig allt om säkerhetshyvlar</h3>

<img src="bilder/säkerhetshyvlar-guide-1.jpg" alt="rakhyvel-och-borste" />

<p>Teknologin har förändrat våra liv drastiskt de senaste 100 åren, oftast till det bättre. När det kommer till rakning har den dock gjort oss en otjänst. Det som dominerar idag är flerbladssystemen, tänk Gillette Mach3, och de har blivit så vanliga tack vare marknadsföringen och teknologin bakom dem, inte för att de presterar bättre.
    Innan säkerhetshyveln så valde de flesta män att få sitt skägg avlägsnat hos barberaren, vilken använde rakkniv. Idén med säkerhetshyvel var att vem som helst skulle kunna raka sig hemma framför spegeln, därav namnet säkerhetshyvel. Först användes en klinga som likt en rakkniv behövde slipas för att hållas skarp, men 1904 introducerade Gillette en variant som använde utbytbara blad. Enkelheten i att använda utbytbara blad gjorde Gillettes uppfinning till en hit.
    <br>
    Några små justeringar har skett sen 1904 men säkerhetshyvlar ser idag nästan likadana ut som de gjorde för över 100 år sen.
    <br>
    Gillette har sen dess gått vidare till flerbladssystem då dessa har en mycket högre vinstmarginal än klassiska rakblad, och tack vare deras högteknologiska utseende tror människor att de ger en bättre rakning än en klassisk säkerhetshyvel, något som inte stämmer.
    <br>
    De senaste åren har man börjat få upp ögonen för säkerhetshyvlar igen tack vare alla fördelar som de har jämfört med flerbladssystem.
</p>

<h3>Fördelar med en säkerhetshyvel</h3>

<img src="bilder/säkerhetshyvlar-guide-2.jpg" alt="gillette-vs-säkerhetshyvel" />

<p><b>En närmare rakning.</b> Dubbelrakblad som används till säkerhetshyvlar är skarpare än flerbladssystem och rakar närmare huden. Genom att vinkla hyveln kan man själv bestämma hur nära bladet ska komma huden.
    <br><br>
    <b>Mindre irritation och inväxta hårstrån.</b> Din hud måste vara skyddad och insmord med tvål eller skum när du rakar dig. På ett flerbladssystem så tar det första bladet bort allt skydd och de efterföljande bladen dras över naken hud vilket leder till irritation och inåtväxande hårstrån.
    <br><br>
    <b>Mycket billigare blad.</b> Då de dubbelrakblad man använder till en säkerhetshyvel är klart billigare än t.ex. Gillette-blad så sparar man snart stora pengar på att byta till en säkerhetshyvel. 8st Gillette Fusion-blad kostar ungefär 250 kronor. 8st dubbelrakblad kostar ungefär 45 kronor.
    <br><br>
    Förutom detta är det mycket trevligare att raka sig med en säkerhetshyvel. Man håller ett ordentligt verktyg av rostfritt stål i handen istället för en bräcklig plasthyvel och tillsammans med en ordentlig rakborste och raktvål så blir rakandet en trevlig stund framför spegeln snarare än en tråkig syssla som måste göras.
    Nackdelar med en säkerhetshyvel
    <br><br>
    Den enda nackdelen egentligen med en säkerhetshyvel är att det finns en inlärningskurva för att bli bra på att raka sig snabbt och effektivt utan skärsår. Under de första veckorna som man rakar sig med en säkerhetshyvel är det viktigt att man är mycket försiktig och tar det långsamt. Hur försiktig man är kommer man antagligen ändå råka skära sig någon gång i början. Att raka sig med säkerhetshyvel är en färdighet som man lär sig. Det man behöver träna in är med vilken vinkel man ska ha på säkerhetshyveln samt att man ska använda minimalt med tryck utan istället låta hyvelns vikt sköta jobbet.
    När man väl fått in snitsen är det som att ha lärt sig cyklar och då rakar man sig snabbt och enkelt och förhoppningsvis helt utan missöden.
</p>

<h3>Hur rakar man sig med en säkerhetshyvel?</h3>

<img src="bilder/säkerhetshyvlar-guide-3.jpg" alt="löddra-rakborste" />

<p>Tillsammans med en säkerhetshyvel använder man en rakborste, två eller skum samt vatten, vilket gör att det kallas för våtrakning. Börja med att blöta rakborsten under varmt vatten för att göra stråna mjuka.
    <br>
    Skaka lite vatten ur borsten och börja löddra upp tvålen. Vi rekommenderar riktig raktvål framför skum då detta ger det bästa glidet. Applicera den löddrande tvålen med cirkelrörelser på skägget. Nästan alla rakborstar använder grävlinghår vilka får skäggstråna att lyfta sig ordentligt.
    <br>
    Raka dig genom att föra hyveln över skäggstråna. Hyveln ska ha en lätt vinkel, ungefär 30 grader, och detta går att justera; ju mer vinkel desto mer blad har kontakt med huden. Du vill inte ha för mycket eller för lite vinkel på hyveln.
    <br>
    Använd mycket lite tryck, tyngden från hyveln ska göra nästan allt jobb. Raka alltid medhårs. Raka vid behov samma område flera gånger. Du kan behöva applicera mer tvål men inte alltid, detta är något man får känsla för med tiden.
</p>

