<h3>Lär dig allt om rakborstar</h3>
<img src="bilder/rakborstar-guide-1.jpg" alt="" />
<p>En rakborste har två huvuduppgifter vid rakning. Den första är att skapa ett rikt och varmt lödder genom att man med borsten vispar luft in i rakskummet eller raktvålen.
    <br /><br />
    Den andra är att mjuka upp och lyfta upp skäggstråna när man applicerar skummet eller tvålen på ansiket.
    <br /><br />
    Förutom detta så värmer den upp huden något vid applicering vilket får porerna att öppna sig samt att den exfolierar hudens yta vilket betyder att man avlägsnar döda hudceler.
    <br /><br />
    Det finns olika typer av hår som kan användas till rakborstar. Exempel är hår från svin eller häst. Det finns också borstar av syntetiskt hår, oftast tillverkade av nylon. Den absolut vanligaste typen av hår i en rakborste är dock grävlinghår. Anledningen till det är den lyxiga känslan av grävlinghåret, dess mycket goda förmåga att suga till sig fukt, dess lena känsla mot huden samt dess slitstyrka.
</p>
<h3>De fyra olika typerna av grävlinghår</h3>
<img src="bilder/rakborstar-guide-2.jpg" alt="" />
<p>Grävlinghår delas in i fyra olika graderingar där hårets kvalitét, uppsugningsförmåga, lenhet samt slitstyrka är det som avgör vad rakborsten får kallas.
    <br /><br />
    <b>Pure badger</b> är det hår som täcker undersidan på grävlingen, vilket är cirka 60 procent av dess hår och är därför den vanligaste typen av grävlinghår. Dessa hår varierar kraftigt i böjlighet, grovhet och färg. Generellt är detta hår grövre än exklusivare typer av hår, och är också den billigaste typen av grävlinghår.
    <br /><br />
    <b>Best badger</b> är hår som är tunnare och böjligare än Pure badger. Detta hår täcker cirka 25 procent av grävlingen och är också längre och ljusare till färgen än Pure badger. Håren i hårknuten är tätare vilket leder till ett rikt lödder och håren är lena vilket känns bekvämt mot huden.
    <br /><br />
    <b>Super badger</b> är ett exklusivare hår än både Pure och Best badger. Håren är egentligen utvalda bland de bästa håren ur graderingen Pure badger så att dess kvalitét är högre än Best badger. Denna gradering är den överlägset mest ovanliga att hitta, kanske därför att de som väljer att köpa en riktigt fin rakborste hellre väljer att lägga ytterligare lite pengar på att få den allra lyxigaste varianten.
    <br /><br />
    <b>Silvertip badger</b> är det hår som växer runt grävlingens nacke. Denna typ av hår är det ovanligaste och dyraste grävlinghåret och används till de mest exklusiva rakborstarna. Håret är ljust, särskilt topparna, därav dess namn. En hårknut av Silvertip-hår ser mer yvig eller “fluffig” ut än de andra typerna av grävlinghår. Detta gör att borsten kan suga upp en stor mängd vatten. Trots det fluffiga utseendet är Silvertip-hår styvare än andra grävlinghår, vilket gör det lite lättare att arbeta upp ett bra lödder samt lite lättare att applicera skummet eller tvålen på ansiktet. Trots dess styvhet känns håret väldigt lent och balanserat mot huden.
</p>
<h3>Underhåll och förvaring</h3>
<img src="bilder/rakborstar-guide-3.jpg" alt="" />
<p>En bra rakborste håller i många år och en silvertip-borste kan hålla i tio år utan problem. Rakborstar tappar alltid hår i början, men efter 10 till 15 användningar så ska den sluta tappa hår sånär som på något hårstrå då och då.
    <br />
    Efter användning kramar du skummet eller tvålen samt vattnet ur borsten ner i handfatet – en gång räcker. Förvara den sedan uppochner i ett rakställ. Håret torkar fort och efter 24 timmar ska den vara torr och redo att användas igen.
    <br /><br />
    Rakgrossisten säljer bara rakborstar av mycket hög kvalitét och vårt mål är att ha 100% kundnöjdhet hos de som köper en rakborste av oss. Tack.
</p>
