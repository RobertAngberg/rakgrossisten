<h1>Rakning utan röda prickar – Vår rakskola!</h1>

<img src="bilder/blogg/roda-prickar-rakning.jpg">

<p>En enkel rakning för att få bort den synliga stubben kan vara snabb och enkel och över på någon minut, men för att få en ordentlig rakning behöver man lite mer tid och kunskap. Här kommer en guide till hur man rakar sig och samtidigt undviker röda prickar och rakbränna.</p>

<h2>Innan rakningen</h2>
<br>
<img src="bilder/blogg/hur-man-rakar-sig.jpg">

<p>Många väljer att tvätta ansiktet innan de rakar sig. För detta är riktig ansiktsrengöring bättre än vanlig tvål. Låt ansiktsrengöringen sitta på i någon minut innan du sköljer.</p>
<p>Dags att applicera rakgel eller raklödder. Låt det sitta på i 2-3 minuter innan du rakar dig eftersom detta mjukar upp hårstråna. Du kan borsta tänderna eller kamma håret under tiden. Använd inte fingrarna för att applicera rakskum eller raklödder – använd en rakborste. Dessa borstar använder oftast grävlingshår för deras förmåga att verkligen få in skummet i skägget, lyfta upp hårstråna och täcka dem med lödder. Detta minskar risken för röda prickar vid rakning.</p>
<p>En bra idé är att raka sig i duschen eller strax efter då detta gör huden varm och fuktig.</p>

<h2>Själva rakningen</h2>
<br>
<img src="bilder/blogg/rakning-roda-prickar.jpg">

<p>För själva rakningen är en vanlig säkerhetshyvel bättre för de flesta än t.ex. Gillette med 4-5 blad. Det viktigaste är att rakbladet du använder är skarpt. Ett vanligt dubbelrakblad till en säkerhetshyvel räcker till en veckas daglig rakning och kostar en bråkdel av vad ett Gillette-rakblad kostar. Ju mer ett blad kostar ju större är risken att du använder det för länge innan du byter det.</p>
<p>Vissa föredrar sig att raka sig med rakkniv, men dessa är generellt svårare att använda och det är lättare att göra illa sig.</p>
<p>Raka medhårs, speciellt om du har väldigt tjocka hårstrån. Vissa hävdar att man får en närmare rakning om man rakar mothårs, men om du har applicerat raklödder/gel ordentligt med en rakborste så ska det inte vara några problem att raka medhårs. Att raka mothårs ökar risken för röda prickar vid rakningen, rakbränna och ingrodda hårstrån.</p>

<h2>Efter rakningen</h2>
<br>
<img src="bilder/blogg/hur-man-rakar-sig1.jpg">

<p>Skölj med kallt vatten eller använd en kall, fuktig handduk för att svalka huden efter rakningen. Grattis, förhoppningsvis har du nu en slätrakad, väldoftande haka utan röda prickar!</p>

