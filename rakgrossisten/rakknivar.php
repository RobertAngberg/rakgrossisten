<h3>Lär dig allt om rakknivar</h3>

<p>En rakkniv ger en närmare rakning än de bästa rakapparaterna och om man gör det korrekt så minimerar de även skärsår och röda prickar. En rakkniv är en något dyrare investering i början men detta lönar sig fort eftersom man inte använder några dyra blad som måste bytas ut.
    <br>
    Att använda en rakkniv får rakandet att kännas som ett hantverk. Det kräver mer koncentration och fokus än rakning med en säkerhetshvel eller rakapparat. Detta ger det hela en meditativ “zen”-känsla när man rakar sig.
    <br>
    Det kräver också mer tid. Själva rakandet borde inte ta längre tid än att raka sig med en säkerhetshyvel, men en rakkniv måste striglas och hållas skarp. Vidare kräver det mer koncentration, vilket kan vara något man måste vänja sig vid om man rakar sig tidigt på morgonen.
</p>

<h3>Vad är skillnaden på rakkniv och shavette?</h3>
<img src="bilder/rakkniv-guide-1.jpg" alt="rakkniv" />
<p>En shavette visas på bilden ovan och är en slags hybrid av en rakhyvel med utbytbara blad och en rakkniv. En shavette är formad som en rakkniv men använder utbytbara blad som en säkerhetshyvel. Shavetter är billigare än rakknivar och är lättare att underhålla då man använder utbytbara blad. De är bra för nybörjare som vill prova på det här med rakknivar, eller för den som inte vill hålla på med underhåll av en vanlig rakkniv. Vanliga dubbelrakblad eller de enkeleggade blad (egentligen bara ett dubbelrakblad brutet itu) som finns till shavetter är alltid väldigt skarpa ur förpackningen, men slits ut efter ett par rakningar.</p>
<h3>Så hur rakar man sig med en rakkniv?</h3>
<img src="bilder/rakkniv-guide-2.jpg" alt="rakkniv" />
<p>Rakning med en rakkniv liknar rakning med en säkerhetshyvel, men din hand och kniv kommer närmare ansiktet, vilket känns mer intimt och precist. Man håller också en rakkniv på ett annorlunda sätt, se bilden. Lägg märke till lillfingrets placering. Det går bra att ha ringfingret där istället om det känns bättre.</p>
<ul>
    <li>Applicera med en rakborste ditt rakskum, eller ännu hellre, en bra raktvål för bästa glid</li>
    <li>Raka medhårs och var stadig men lätt på handen</li>
    <li>Många gillar att sträcka ut huden med den lediga handen</li>
    <li>Vid behov, raka igen på samma område</li>
</ul>
<h3>Vad är en strigel och hur används den?</h3>
<img src="bilder/rakkniv-guide-3.jpg" alt="rakkniv" />
<p>En strigel är en avlång bit läder som används för att strigla din rakkniv. Strigeln har en grov sida och en slät sida. Den släta sidan av läder används oftast och den använder du för att strigla kniven varje gång innan rakning.
    Det är en skillnad på att slipa och att strigla. Om man tittar på en rakkniv i ett mikroskop så ser man att eggen består av flera punkter som ser ut liknar tänderna på en kam. En ostriglad egg ser i mikroskopet ut som en trasig kam med ojämna tänder som pekar åt olika håll. Striglandet återställer dessa punkter till sin korrekta position.
    <br><br>
    Den grövre sidan används för grövre slipning och behövs inte lika ofta. Om du tittar noga på knivens egg och du med ögat kan se att det finns ojämnheter, märken eller liknande så bör du använda den grövre sidan på strigeln innan du använder den slätare lädersidan för strigling.
    Många gillar också att använda den grövre baksidan efter rakning några drag för att rengöra kniven och hålla bort all fukt och ansatser till rost.
    <br><br>
    En strigel har ofta en ögla i ena änden – denna sätter du över exempelvis en handdukskrok i badrummet. Detta gör att du bara behöver en hand för att hålla fast strigeln samtidigt som den andra handen utför striglandet.
</p>
<h3>Hur man striglar sin rakkniv</h3>
<img src="bilder/rakkniv-guide-4.jpg" alt="rakkniv" />
<p>När du striglar för du kniven framåt längs lädret med bladets rygg först, framåt i riktning med din rörelse. Du gör alltså inte rörelsen med eggen först, det kommer att skada din strigel och din rakkniv.
    <br>
    När du kommer till slutet på strigeln så vänder/rullar du kniven på bladets rygg utan att lyfta kniven från lädret. Kniven ska du alltså rulla ett halv varv så att du kan föra kniven tillbaka mot dig med ryggen först. Sen vänder/rullar du kniven igen och börjar från början.
    <br>
    En rörelse över lädret kallas ett “pass”. När du striglar rekommenderas 25 till 50 pass för att få eggen riktigt skarp och jämn innan du börjar raka dig. Med lite övning bör det hela inte ta mer än två minuter.
    <br><br>
    Vi hoppas att denna guide besvarar de frågor du kan ha haft angående rakkninvar. Du är välkommen att kontakta oss om du undrar något mer eller om du har förslag på något som vi borde lägga till i denna guide.
</p>

